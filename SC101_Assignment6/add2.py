import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    cur = l1
    l1_lst = []
    l1_str = ""
    while cur is not None:
        l1_lst.append(str(cur.val))
        cur = cur.next
    for i in range(len(l1_lst)):
        l1_str += l1_lst.pop()

    cur = l2
    l2_lst = []
    l2_str = ""
    while cur is not None:
        l2_lst.append(str(cur.val))
        cur = cur.next
    for i in range(len(l2_lst)):
        l2_str += l2_lst.pop()

    l3_lst = []
    l3_int = int(l1_str)+int(l2_str)      # l3_int is an int
    for i in range(len(str(l3_int))):
        ch = str(l3_int)[len(str(l3_int))-i-1]
        l3_lst.append(int(ch))

    l3 = ListNode(l3_lst[0], None)
    cur = l3
    for i in range(len(l3_lst)-1):
        cur.next = ListNode(l3_lst[i+1], None)
        cur = cur.next
    return l3


